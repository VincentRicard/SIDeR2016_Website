    <div class="three back">
      <h4>Opening keynote</h4>
      <h5>David Cuartielles</h5>
      <p>
        David Cuartielles is one of the co-founders of the open source platform Arduino, and teaches interactive technologies at Malmö University’s School of Arts and Communication.
      </p>
    </div>
    <div class="three five-left">
      <h4>Hybrid Matters</h4>
      <h5>Åsa Ståhl and Kristina Lindström</h5>
      <p>
        Design was called into being at roughly the same time as a strong and hierarchical division between nature and humans were enforced. Is our contemporary time, what some call the Anthropocene epoch, rather a call for rethinking and reworking these divisions and how designers work? <br>
        Lindström and Ståhl have been hosting public engagement events on two hybrid matters: plastiglomerates, which are a combination of a stone and plastics, and domestic composting of styrofoam with common mealworms. Both these hybrid matters trouble the division between nature and humans as well as the natural and the artificial, but suggest different ways of living with or without plastics in the future. <br>
        In this keynote Lindström and Ståhl will discuss these two hybrid matters as both resources for new design opportunities and from the perspective of co-living where species can flourish together. <br>
        See more on the research project Hybrid Matters.
      </p>
    </div>
