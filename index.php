    <p class="three">The theme of the 12th Student Interaction Design Research conference deals with the ever evolving relationship between Human and Nature. Interaction design is at the centre of this relationship as technology and design progressively transform, blur and redefine our notion of the natural and the artificial.<br>
    We would therefore like to invite all interaction design students to take part in SIDeR 2016 and welcome a broad range of design work that touches upon the aspects of nature in human behaviours, bodies and surroundings.</br>
    SIDeR 2016 will take place on the 1st and 2nd of April, 2016 and will be held at Malmö University, Sweden. The two day event will be a mixture of keynote lectures, demos, workshops and parties.</p>
