  <section id="workshops">
    <h3 class="three">Workshops</h3>
    <div class="three back one-left">
      <h4>Using Brainwaves: How Brain­Computer Interfaces can enhance our everyday environment</h4>
      <h5>Marcus Johansson, IOIO Malmö</h5>
      <p>This workshop aims to increase awareness for how Brain­Computer Interfaces (BCI) can enhance and help in the everyday environment, and to show the limitations of learning how to interact and use BCI´s. <br>
        Electroencephalographic (EEG) scans from our brain can be used as input to electromechanical systems, this type of interface is called brain­computer interface.
        The ability of computers to enhance and augment both mental and physical abilities and its potential is no longer a science fiction. For the past few decades, human has fantasized to communicate and interact with machines via thoughts itself and moreover expectation was that the devices will be able to reveal human minds and attention as well. Until the recent years the advancement of Information technology, cognitive neuroscience and brain imaging capturing technologies allows for the interaction with human brain directly.
        The use of brain imaging capturing sensors make it possible to monitor parts of the process that runs in our brains and eventually relate it to certain forms of thoughts. By using the capabilities of BCI it is possible to see how a person brain reacts on different stimuli, whether it’s from something outside and physical or internal and mental. BCI can not only view and monitor these reactions, but also use them to enhance and help the user.
      </p>
    </div>
    <div class="three one-left">
      <h4>Food – Unfood</h4>
      <h5>Zeenath Hasan</h5>
      <p>The rise of the big food industry framed us as consumers of the food on our plates. The anti globalisation movement gave rise to the activist eater. More recent urban guerilla moves to reclaim food open up for the DIY consumer/eater. But have we not always been eating food? <br>
        In this workshop we take a designerly look at food as interaction. The workshop unfolds as a cook and talk session. Participants are required to bring with them ingredients for a meal. The condition being that none of the ingredients have been paid for with money. <br>
        Zeenath Hasan practices participatory design for designerly social engagement. Following a decade long career as an ethnographer for multinational technology manufacturers, she currently devotes her time between mobilizing citizen based initiatives for inclusive culinary practices and teaching in design schools. <br>
        As part of her socially engaged design practice, Zeenath founded Rude Food Malmö, Sweden’s 1st food waste catering service and restaurant. Initiated in October 2014, it runs as a not-for-profit, citizen action network to counter food waste in Malmö. The network manifests as food rescue, food waste upcycling, food waste knowledge building and circulation action.
      </p>
    </div>
    <div class="three back">
      <h4>Counterfactual Natures</h4>
      <h5>Anuradha Reddy, Nils Ehrenberg</h5>
      <p>“‘Counterfactuals’ are central to the theory of possible worlds.” <br>
        In this workshop, we will together explore potential, plausible, and possible futures in our interactions with technological devices. The aim is to critically inquire about the preconditioned ways in which we use technological artefacts, to “subvert a single authoritative interpretation of a system in the service of provoking people to arrive at their own self-­determined understanding” (Wakkary et al, 2015). <br>
        We will present and interact with a series of design artefacts that combine everyday objects and modules from PELARS, an ongoing european research project exploring learning and building with hardware at Malmö University (Learning+Making, n.d.). These artefacts will be designed to exhibit peculiar behaviours that challenge original assumptions and thus support the creation of new interactions. What kind of design spaces and alternative fictions might we enter while encountering the unfamiliar? The workshop will be a collaborative effort to generate new configurations of use with the PELARS tool­kit, while situating it in the everydayness of the world.
      </p>
    </div>
    <div class="three two-left">
      <h4>Odlingen / The Cultivation</h4>
      <h5>Gunnel Pettersson</h5>
      <p>In this workshop we make buckwheat noodles (soba). The craft-session is followed by a conversation around the experience of artistic methods, design processes, interaction and intercultural communication strategies as methods of change. <br>
        Odlingen/The Cultivation is an interdisciplinary project using buckwheat as a source of inspiration. The project Buckwheat Österlen has initiated cultivation of buckwheat in the southeastern part of Sweden. By taking up cultivation of a forgotten culture plant, areas as new livelihoods, innovation, exchange of knowledge and democratic aspects of the rural-urban public space has been problematized. <br>
        Gunnel Pettersson is an artist and senior lecturer at the School of Arts and Communication (K3) at Malmö University.
      </p>
    </div>
    <div class="three back one-left">
      <h4>Micro Gamejam: Exploring Playful Relationships Between Humans and Insects</h4>
      <h5>Michelle Westerlaken</h5>
      <p>How would you describe your relationship with insects? Specifically with ants? As humans we usually don’t relate to the kinds of animals we don’t recognize ourselves in such as snails, anchovies, frogs, or bees. However, ecological systems on this planet are dependent on these animals and we are currently investigating new ways to ensure that these kinds of species do not go extinct. Is our interest in caring for specific species solely based on their attractiveness and the amount of similarities we share with them? What if we can start to change our relationships with insects by exploring their playfulness together? <br>
        In this workshop we will explore and discuss this topic through the design of playful interactions with ants taking a post-humanist perspective. We will use different materials to rapidly prototype playful interactions and games that aim to be of interest to both ants and humans. Then we will try out the prototypes we created with an actual Lasius Niger colony (the regular black ant) and discuss our experiences and the types of questions that these practices could propose.
      </p>
    </div>
