    <h3 class="two one-left">Call for participation</h3>
    <p class="three back">
      SIDeR 2016 invites a broad range of original unpublished research and design work that connects to the theme Human | Nature. There is a list with suggested themes above, but the theme can be interpreted freely.</br>
      We invite three different types of submissions:
    </p>
    <div class="two two-left">
      <h4>Paper</h4>
      <p>Papers must not exceed 4 pages (~2000 words) following the two-column ACM SIGCHI format. Papers will be reviewed by members of the Program Committee, as well as by peers. Accepted papers will be presented orally at the conference during the paper sessions.</p>
    </div>
    <div class="two one-left">
      <h4>Posters</h4>
      <p>A1 posters should be submitted as a pdf file and must include a description of the content, which must not exceed 2 pages following the two-column ACM SIGCHI format. Posters will be presented during the demo and poster presentation session at the conference.</p>
    </div>
    <div class="two two-left">
      <h4>Demos</h4>
      <p>Demos can be submitted by sending a video and a technical description, which must not exceed 2 pages following the two-column ACM SIGCHI format. Authors must provide a link in the paper (just before the References) to view the video (e.g. from Youtube or Vimeo). The video may be password protected – in that case, please include the password with the link. Demos will be presented during the demo and poster presentation session at the conference.</p>
    </div>
