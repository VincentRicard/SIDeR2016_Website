    <h3 class="three one-left">Best Paper Award</h3>
    <h4 class="two back one-left">The SIDeR’16 Best Paper Award in cooperation with NordiCHI’16</h4>
    <p class="four back">
      NordiCHI is the premiere venue for presentation of research in the areas of interaction design and human computer-interaction in Europe and attracts researchers from all over the world. In 2016 the conference will be organized in Gothenburg, Sweden Oct 23-27, 2016, see www.nordichi2016.org. NordiCHI is organized in collaboration with ACM SIGCHI, and it is the meeting place for researchers, designers, practitioners, and educators from a broad range of traditions and communities from all around the world. Historically there are strong connections between SIDeR and the organizers of NordiCHI 2016. For instance SIDeR has taken place in Gothenburg twice, 2006 and 2012. <br>
      To strengthen the ties between SIDeR and NordiCHI, and provide students with an opportunity to present their work at a well-established research conference, we are happy to announce the SIDeR Best Paper Award. <br>
      The SIDeR’16 Best Paper award is targeted to master and bachelor students in interaction design with an accepted contribution at SIDeR’16. All three categories; Papers, Posters and Demos will be evaluated. The criteria used for selecting the best paper are: Significance of the contribution to the field, Creativity, Rigor, Novelty, and Clarity of written presentation.
    </p>
    <div class="three back">
      <p>
        The SIDeR’16 best Paper has the following goals :
      </p>
      <ul>
        <li>to give bachelor and master students the opportunity to share their research ideas and results at NordiCHI and provide visibility for their work</li>
        <li> to give students the opportunity to meet with and interact with NordiCHI attendees to share ideas and gain new insights</li>
        <li>to give students an opportunity to sharpen their communication skills</li>
        <li>to give feedback to students about their research from industry and academia</li>
        <li>to recognize and reward outstanding student research</li>
      </ul>
    </div>
    <p class="two back one-left">
      The winner will be selected by a jury consisting of members of the SIDeR and NordiCHI program and organizing committees. The selected winner will receive a diploma, and an invitation to present the work for an international research community at NordiCHI’16 in Gothenburg, Sweden.
    </p>
